    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed

        try {

            String search1 = txtsubsearch.getText();

            String str;

            str = "Select * from subject where Subject_ID='" + search1 + "'";

            rs = stmt.executeQuery(str);

            rs.next();
            txtsubid.setText(rs.getString("Subject_ID"));
            txtsubname.setText(rs.getString("Subject_Name"));
            txtgrade.setText(rs.getString("Grade"));
            txtteacherid.setText(rs.getString("Teacher_ID"));

        } catch (SQLException ex) {
            btndelete.setEnabled(false);
            JOptionPane.showMessageDialog(null, "Invalid Subject ID !!!\n\n");

        }

    }//GEN-LAST:event_btnsearchActionPerformed

